    private void allActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allActionPerformed
        // TODO add your handling code here:
        String url="jdbc:oracle:thin:@localhost:1521:medisoft";
       try {
           Class.forName("oracle.jdbc.driver.OracleDriver");
            con=DriverManager.getConnection(url,"hr","hrmedi");
       } catch (ClassNotFoundException ex) {
          System.out.println("Driver not loaded");
       } catch (SQLException ex) {
           System.out.println("connection not stablish"+ex);
       }
       
       
        try {
             ps=con.prepareStatement("select * from medicine");
             rs=ps.executeQuery();
            tm=(DefaultTableModel) meditable.getModel();
           tm.setRowCount(0);
           while(rs.next()){
           Object o[] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
           tm.addRow(o);
            }
        }catch (SQLException ex) {
            Logger.getLogger(AddNewStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_allActionPerformed

