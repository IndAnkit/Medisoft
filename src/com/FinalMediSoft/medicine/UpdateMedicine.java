    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String url="jdbc:oracle:thin:@localhost:1521:medisoft";
       try {
           Class.forName("oracle.jdbc.driver.OracleDriver");
            con=DriverManager.getConnection(url,"hr","hrmedi");
       } catch (ClassNotFoundException ex) {
          System.out.println("Driver not loaded");
       } catch (SQLException ex) {
           System.out.println("connection not stablish"+ex);
       }
       
       
        try {
           // int mobile=Integer.parseInt(smobile.getT());
            ps=con.prepareStatement("insert into medicine values(?,?,?,?,?,?)");
            
            ps.setString(1,mbatchno.getText());
            ps.setString(2,mname.getText());
            ps.setString(3,mcompany.getText());
            ps.setString(4,mquantity.getText());
            ps.setString(5,mprice.getText());
            SimpleDateFormat sdf=new SimpleDateFormat("YYYY-MM-DD");
            String date=sdf.format(edate.getDate());
            //ps.setDate(5,sjoindate.getText());
            ps.setString(6,date);
             ps.executeUpdate();
            con.close();
            JOptionPane.showMessageDialog(this,"saved successFully");
            mname.setText("");
            mcompany.setText("");
            mprice.setText("");
            mquantity.setText("");
            mbatchno.setText("");
            
            
        } catch (SQLException ex) {
            Logger.getLogger(AddNewStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_updateActionPerformed

